from abc import ABC, abstractmethod
from dataclasses import dataclass
from typing import Dict, Optional
import re

@dataclass
class AccentChar:
    char: str  # Single character string
    accent: str


@dataclass
class SpecialFontChar:
    letter: str  # UppercaseLetter or specific LaTeX commands
    font_cmd: str  # LaTeX font command


class UTF8ToLtXConverter(ABC):
    @abstractmethod
    def convert(self, char: str) -> str:
        pass


class HashUTF8ToLtXConverter(UTF8ToLtXConverter):
    def convert(self, char: str) -> str:
        is_alphanumeric = re.match(r'[a-z0-9]', char, re.IGNORECASE)
        if is_alphanumeric:
            return char

        accent_char = vowels_with_accents.get(char)
        if accent_char:
            ltx_cmd = self.convert_accent_char_to_ltx(accent_char)
            if ltx_cmd:
                return ltx_cmd
            return char

        special_char = self.convert_special_char_to_ltx(char)
        if special_char:
            return special_char

        return char

    def convert_accent_char_to_ltx(self, accent_char: AccentChar) -> Optional[str]:
        cmd = accent_to_ltx_cmd.get(accent_char.accent)
        if not cmd:
            return None
        return f"\\{cmd}{{{accent_char.char}}}"

    def convert_special_char_to_ltx(self, char: str) -> Optional[str]:
        special_font_char = special_font_chars.get(char)
        if not special_font_char:
            return None
        return f"\\{special_font_char.font_cmd}{{{special_font_char.letter}}}"


